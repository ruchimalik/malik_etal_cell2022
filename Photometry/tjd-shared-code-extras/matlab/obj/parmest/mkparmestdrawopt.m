function dopt = mkparmestdrawopt(varargin)
% MKPARMESTDRAWOPT display options for drawparmest
  
  dopt = struct(...
      'sharpen', [],...
      'scale', [],...
      'autoscale', [],...
      'gwtime', [],...
      'gwparm', [],...
      'marginalize', false,...
      'binarize', false,...
      ...
      'modemap', false,...
      'modethresh', -Inf,...
      ...
      'drawparm', true,...
      'drawlegend', true,...
      'drawscalebar', true,...
      'drawxaxis', true,...
      'drawyaxis', true,...
      ...
      'dispname', [],...
      'RGBchan', [],... % map into R,G, or B channels (1-3); ignore cmap
      'cmap',hot(64),... % plain old 'hot' opens a figure
      'cmapwin', [],... % use whole cmap
      'cmapwin_whitebg', [],...
      'datalim', [0 1]); % limits of data to put in cmapwin
      
  dopt = parseArgsLite(varargin, dopt);

  if dopt.modemap && ...
        (~isempty(dopt.gwtime) ||...
         ~isempty(dopt.gwparm)),
    error('if ''modemap'' requested, no filtering is allowed');
  end  