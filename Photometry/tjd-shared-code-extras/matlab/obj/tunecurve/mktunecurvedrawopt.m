function opt = mktunecurvedrawopt(varargin)
% MKTUNECURVEDRAWOPT options for plotting tuning curves
  
% take in tunecurveopt
% polar plots
opt = struct('plottype','updownarea',... % or 'bars', or 'stacked'
             'plotsperfig', 8,...
             'units', 'm',... % or 'cm'
             'leftlabel', true,...
             'botlabel', true,... % only for bottom plot of the fig
             'drawarrows', true,...
             'ylims',[],...
             'ylimsequal', true,...
             'ylogscale', false,...
             'bgcol', [0.9 0.9 0.9],...
             'col', [1 0 0; 0 0 1]); % blue, red

opt = parseArgs(varargin, opt);